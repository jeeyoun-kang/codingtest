SELECT A.AUTHOR_ID, AUTHOR_NAME,A.CATEGORY,SUM(B.SALES*A.PRICE) AS TOTAL_SALES
FROM BOOK A JOIN BOOK_SALES B ON A.BOOK_ID = B.BOOK_ID
JOIN AUTHOR C ON A.AUTHOR_ID = C.AUTHOR_ID
WHERE DATE_FORMAT(SALES_DATE,'%Y-%m') IN ('2022-01')
-- WHERE SALES_DATE BETWEEN '2022-01-01' AND '2022-01-31'
-- WHERE SALES_DATE LIKE "2022-01%'
GROUP BY A.AUTHOR_ID,A.CATEGORY
ORDER BY AUTHOR_ID, A.CATEGORY DESC;